'''Functions related to data types & structures'''


bool(1)                                     # -> True
float(True)                                 # -> 1.0
int(True)                                   # -> 1
str(True)                                   # -> True
list((1,2,3))                               # -> [1, 2, 3]
dict((('a', 'A'), ('b', 'B'), ('c', 'C')))  # -> {'c':'C', 'a':'A', 'b':'B'}
set(['a', 'b', 'c'])                        # -> {'b', 'c', 'a'}
frozenset(['a', 'b', 'c'])                  # -> {'c', 'b', 'a'}
tuple(['a', 'b', 'c'])                      # -> ('a', 'b', 'c')
 
